"""
Unit 3: Probability Distributions หน้า 24
กิจกรรมที่ 3
คุณกําลังบรรยายให้กับกลุ่มนักเรียน 1000 คน 
คุณขอให้พวกเขาแต่ละคนสุ่มเลือกจํานวนเต็มระหว่าง 1 ถึง 10 
สมมติว่าการเลือกของพวกเขาเป็นแบบสุ่มอย่างแท้จริง
อะไรคือสิ่งที่คาดเดาได้ดีที่สุดสําหรับจํานวนนักเรียนที่เลือกหมายเลข 9
คุณคาดหวังว่านักเรียนกี่เปอร์เซ็นต์ที่เลือกตัวเลขที่น้อยกว่าหรือเท่ากับ 6
"""
# กำหนดจำนวนผู้เรียน
num_students = 1000

# ความน่าจะเป็นที่เลือกแต่ละหมายเลข (1 ถึง 10)
probability_per_number = 1 / 10  # ทุกหมายเลขมีความน่าจะเป็นเท่ากัน

# คำนวณจำนวนผู้เรียนที่เลือกหมายเลข 9
students_picking_9 = num_students * probability_per_number

# คำนวณเปอร์เซ็นต์ของผู้เรียนที่เลือกหมายเลข <= 6
probability_less_than_equal_6 = 6 / 10  # หมายเลข 1 ถึง 6 มีทั้งหมด 6 หมายเลข
percentage_less_than_equal_6 = probability_less_than_equal_6 * 100

# แสดงผลลัพธ์
print("จำนวนผู้เรียนที่คาดว่าจะเลือกหมายเลข 9: {:.0f}".format(students_picking_9))
print("เปอร์เซ็นต์ของผู้เรียนที่คาดว่าจะเลือกหมายเลข <= 6: {:.2f}%".format(percentage_less_than_equal_6))

# - num_students: จำนวนผู้เรียนทั้งหมด (1000 คน)
# - probability_per_number: ความน่าจะเป็นของการเลือกแต่ละหมายเลข (1/10 เพราะมี 10 หมายเลข)
# - students_picking_9: คำนวณจำนวนผู้เรียนที่เลือกหมายเลข 9 โดยการคูณความน่าจะเป็นกับจำนวนผู้เรียนทั้งหมด
# - probability_less_than_equal_6: ความน่าจะเป็นของการเลือกหมายเลข <= 6 (6 หมายเลขจากทั้งหมด 10)
# - percentage_less_than_equal_6: แปลงความน่าจะเป็นเป็นเปอร์เซ็นต์
