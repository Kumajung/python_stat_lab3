"""
Unit 3: Probability Distributions หน้า 23
กิจกรรมที่ 2: จํานวนเรือที่จะมาถึงท่าเรือในวันใดวันหนึ่งเป็นตัวแปรสุ่มที่แสดงด้วย x การแจกแจงความน่าจะเป็นสําหรับ x คือ:
x 10 11 12 13 14
P(x) .4 .2 .2 .1 .1
ค้นหาความน่าจะเป็นที่ในวันหนึ่ง: 
a. เรือ 14 ลํามาถึง
b. มีเรืออย่างน้อย 12 ลํามาถึง 
c. เรือมาถึงสูงสุด 11 ลํา
"""

# กำหนดการแจกแจงความน่าจะเป็น (Probability Distribution)
probabilities = {
    10: 0.4,
    11: 0.2,
    12: 0.2,
    13: 0.1,
    14: 0.1
}

# คำนวณความน่าจะเป็นในกรณีต่าง ๆ
# a. ความน่าจะเป็นที่มีเรือมาถึง 14 ลำในวันหนึ่ง
p_exactly_14 = probabilities[14]

# b. ความน่าจะเป็นที่มีเรือมาถึงอย่างน้อย 12 ลำ
p_at_least_12 = sum(probabilities[x] for x in probabilities if x >= 12)

# c. ความน่าจะเป็นที่มีเรือมาถึงมากที่สุด 11 ลำ
p_at_most_11 = sum(probabilities[x] for x in probabilities if x <= 11)

# แสดงผลลัพธ์
print("a. ความน่าจะเป็นที่มีเรือมาถึง 14 ลำ: {:.2f}".format(p_exactly_14))
print("b. ความน่าจะเป็นที่มีเรือมาถึงอย่างน้อย 12 ลำ: {:.2f}".format(p_at_least_12))
print("c. ความน่าจะเป็นที่มีเรือมาถึงมากที่สุด 11 ลำ: {:.2f}".format(p_at_most_11))

# - สร้าง dictionary ที่เก็บการแจกแจงความน่าจะเป็นสำหรับจำนวนเรือที่มาถึงในแต่ละวัน
# - ใช้ sum() เพื่อรวมความน่าจะเป็นตามเงื่อนไขที่กำหนด
# - กรณี a: ดึงความน่าจะเป็นของ x = 14 โดยตรง
# - กรณี b: รวมความน่าจะเป็นของ x >= 12
# - กรณี c: รวมความน่าจะเป็นของ x <= 11

